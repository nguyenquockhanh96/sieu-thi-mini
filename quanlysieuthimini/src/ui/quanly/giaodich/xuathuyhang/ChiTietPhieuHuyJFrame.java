    /*
     * Hàm đổ các thông tin mặc định 
     */
    void doThongTin() {
        try {
            // khởi tạo ngày huỷ
            dat_NgayHuy.setDate(PH.getNgayHuy());
            // đổ mã nhân viên đang thao tác vào textfield
            txt_MaNhanVien.setText(PH.getMaNhanVien());
            // đổ mã phiếu kiểm
            txt_MaChungTu.setText(PH.getMaPhieuHuy());
            // đổ loại phiếu huỷ
            if (PH.getLoaiPhieuHuy() == 0)
                rad_HuyHetHSD.setSelected(true);
            else
                rad_HuyHangLoi.setSelected(true);
            // số lượng huỷ
            txt_TongSoLuong.setText(PH.getSoLuongHuy()+"");
            // giá trị huỷ
            txt_TongGiaTri.setText(dcf.format(PH.getGiaTriHuy()));
            // ghi chú
            txa_GhiChu.setText(PH.getGhiChu());
            // đổ thông tin dssp vào treemap
            List<PhieuHuyChiTiet> listPHCT = PHCTDAO.timDanhSachPHCT(PH.getMaPhieuHuy());
            for (PhieuHuyChiTiet PHCT : listPHCT) {
                SanPham SP = SanPhamDAO.timTheoMaSP(PHCT.getMaSanPham());
                DSSP.put(SP, PHCT.getSoLuong());
            }
            capnhatDanhSachHuy();
        } catch (Exception e) {
            DialogHelper.alert(this, "Có lỗi: " + e.toString());
        }
    }
    
    /*
     * Hàm cập nhật danh sách huỷ
     */
    void capnhatDanhSachHuy() {
        DefaultTableModel dtm = (DefaultTableModel) tbl_ChiTietPhieuHuy.getModel();
        dtm.setRowCount(0);
        int STT = 1;
        for (SanPham SP : DSSP.keySet()) {
            DonViTinh DVT = DVTDAO.timTheoMa(SP.getMaDVT());
            Object[] row = {STT++,
                            SP.getMaSanPham(),
                            SP.getTenSanPham(),
                            DVT.getMoTa(),
                            dcf.format(SP.getGiaVon()),
                            DSSP.get(SP),
                            dcf.format(DSSP.get(SP)*SP.getGiaVon())
                            };
            dtm.addRow(row);
        }
    }
    
