    KhachHangDAO KhachHangDAO = new KhachHangDAO();
    ThongKeDAO ThongKeDAO = new ThongKeDAO();
    
    /*
     * Hàm khởi tạo các giá trị ban đầu của Frame
     */
    void inIt(){
        this.setSize(1366, 768);
        this.setMinimumSize(new Dimension(1366, 768));
        this.setMaximumSize(new Dimension(1366, 768));
        setResizable(false);
        setLocationRelativeTo(null);
        capnhatDuLieu();
        btn_NgungGiaoDich.setVisible(false);
    }
    
    /*
     * Hàm cập nhật dữ liệu khách hàng
     */
    void capnhatDuLieu(){
        try {
            List<KhachHang> listKH = KhachHangDAO.select();
            doDuLieuVaoBang(listKH);
        } catch (Exception e) {
            DialogHelper.alert(this, "Có lỗi: " + e.toString());
        }
    }
    
    /*
     * Hàm tìm khách hàng có sinh nhật trong tháng
           lọc khi select checkbox 'Sinh nhật trong tháng'
     */
    void timTheoSinhNhat(){
        try {
            List<KhachHang> listKH = KhachHangDAO.timTheoSinhNhat();
            doDuLieuVaoBang(listKH);
        } catch (Exception e) {
            DialogHelper.alert(this, "Có lỗi: " + e.toString());
        }
    }
    
    /*
     * Hàm tìm khách hàng có trạng thái 'Ngưng giao dịch'
     */
    void locTheoTrangThai(){
        try {
            List<KhachHang> listKH = KhachHangDAO.timKHNgungHoatDong();
            doDuLieuVaoBang(listKH);
        } catch (Exception e) {
            DialogHelper.alert(this, "Có lỗi: " + e.toString());
        }
    }

    /*
     * Hàm đổ dữ liệu vào bảng tbl_DanhSachKhachHang
     */
    void doDuLieuVaoBang(List<KhachHang> listKH) throws SQLException{
        DefaultTableModel dtm = (DefaultTableModel) tbl_DanhSachKhachHang.getModel();
        dtm.setRowCount(0);
        for(KhachHang KH : listKH){
            float TongGiaoDich = ThongKeDAO.tongGiaoDichKH(KH.getMaKhachHang());
            Object[] row = {false, 
                            KH.getMaKhachHang(),
                            KH.getHoTen(),
                            KH.getDienThoai(),
                            TongGiaoDich,
                            KH.getDiemThuong(),
                            KH.getTrangThai() ? "Đang giao dịch" : "Ngưng giao dịch"
                            };
            dtm.addRow(row);
        }
    }
    
    /*
     * Hàm tìm khách hàng theo ngày giao dịch cuối cùng
     */
    void timTheoGDCuoi() {
        String NgayTim = DateHelper.toString(dat_GiaoDichCuoi.getDate(), "yyyy-MM-dd");
        if (!NgayTim.isEmpty()) {
            try {
                List<KhachHang> listKH = KhachHangDAO.timTheoGDCuoi(NgayTim);
                doDuLieuVaoBang(listKH);
            } catch (Exception e) {
                DialogHelper.alert(this, "Có lỗi: " + e.toString());
            }
        }
    }
    
    /*
     * Hàm tìm tổng hợp theo mã, tên, SĐT
     */
    void timTongHop() {
        String ChuoiTimKiem = txt_TimTheoMaTenSDT.getText();
        if (!ChuoiTimKiem.isEmpty()) {
            try {
                List<KhachHang> listKH = KhachHangDAO.timTheoMaTenSDT(ChuoiTimKiem);
                doDuLieuVaoBang(listKH);
            } catch (Exception e) {
                DialogHelper.alert(this, "Có lỗi: " + e.toString());
            }
        }
        else {
            capnhatDuLieu();
        }
    }
 
    // list chua ma khach hang duoc chon de ngung giao dich
    static List<String> listKH = new ArrayList<>();
    
    /*
     * Hàm tạo danh sách các khách hàng đang được chọn trên tbl_DanhSachKhachHang
     */
    void taoDanhSachDangChon() {
        int i = tbl_DanhSachKhachHang.getSelectedRow();
        if(i >= 0){
            String MaKH = tbl_DanhSachKhachHang.getValueAt(i, 1).toString();
            if((Boolean)tbl_DanhSachKhachHang.getValueAt(i, 0) && !MaKH.equalsIgnoreCase("KH0")){
                if(!(listKH.indexOf(MaKH) >= 0)) {
                    listKH.add(MaKH);
                }
            }
            else {
                if((listKH.indexOf(MaKH) >= 0)){
                    listKH.remove(MaKH);
                }
            }
        }
        if (!listKH.isEmpty()) {
            btn_NgungGiaoDich.setVisible(true);
        }
        else {
            btn_NgungGiaoDich.setVisible(false);
        }
    }
     
    /*
     * Hàm cập nhật trạng thái ngưng giao dịch cho khách hàng
     */
    void ngungGiaoDich() {
        if(DialogHelper.confirm(this, "Chuyển trạng thái ngưng giao dịch cho các khách hàng này?")) {
            try {
                int i = 0;
                for(String MaKH : listKH) {
                    i = KhachHangDAO.ngungGiaoDich(MaKH);
                }
                if(i > 0) {
                    DialogHelper.alert(this, "Cập nhật thành công");
                    listKH.clear();
                    btn_NgungGiaoDich.setVisible(false);
                    capnhatDuLieu();
                }
                else {
                    DialogHelper.alert(this, "Cập nhật thất bại");
                }
            } catch (Exception e) {
                DialogHelper.alert(this, "Có lỗi: " + e.toString());
            }
        }
    }
    
    /*
     * Hàm mở frame cập nhật khách hàng 
          và truyền model KhachHang đang được chọn qua frame cập nhật
     */
    void capnhatKhachHang() {
        int index = tbl_DanhSachKhachHang.getSelectedRow();
        String MaKH = tbl_DanhSachKhachHang.getValueAt(index, 1).toString();
        if (!MaKH.equalsIgnoreCase("KH0")) {
            KhachHang KH = KhachHangDAO.timTheoMa(MaKH);
            new CapNhatKhachHangJFrame(KH).setVisible(true);
        }
        else {
            DialogHelper.alert(this, "Không được quyền sửa khách mặc định");
        }
    }

