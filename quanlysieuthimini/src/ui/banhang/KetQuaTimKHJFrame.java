    void hienThongTinKhachHang(){
        DefaultTableModel dtm = (DefaultTableModel) tbl_DanhSachKhachHang.getModel();
        dtm.setRowCount(0);
        for (KhachHang KhachHang : ListKH){
            Object[] row = {
                KhachHang.getMaKhachHang(),
                KhachHang.getHoTen(),
                KhachHang.getDiemThuong(),
                KhachHang.getDienThoai(),
                KhachHang.getDiaChi()
            };
            dtm.addRow(row);
        }
    }
    
    void chonKhachHang(){
        // lấy index của dòng được chọn
        int indexRowSelected = tbl_DanhSachKhachHang.getSelectedRow();
        // lấy mã khách hàng
        String MaKhachHang = tbl_DanhSachKhachHang.getValueAt(indexRowSelected, 0).toString();
        // truy vấn đối tượng khách hàng để truyền vào biến toàn cục KhachGiaoDich
        try {
            ShareHelper.KhachGiaoDich = KhachHangDAO.timKhachHangTheoMa(MaKhachHang);
            // truyền mã khách hàng vào hoá đơn đang giao dịch
            ShareHelper.HoaDonGiaoDich.setMaKhachHang(ShareHelper.KhachGiaoDich.getMaKhachHang());
            chuyenThongTinVeFrameBanHang();
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Có lỗi truy vấn: " + e.toString());
        }
    }
    
    /*
     * Hàm này tìm các frame đang mở và duyệt đến frame BanHang
     *    Reload lại frame BanHang để hiển thị thông tin khách đang giao dịch
     */
    void chuyenThongTinVeFrameBanHang(){
        Frame[] listFrames = Frame.getFrames();
        for (Frame fr : listFrames) {
            String tenFrame = fr.getClass().getName();
            if (tenFrame.equals("ui.banhang.BanHangJFrame")) {
                fr.dispose();
            }
        }
        new BanHangJFrame().setVisible(true);
    }
    
    /*
     * Hàm tìm kiếm khách hàng theo SDT hoặc họ tên
     *    Nếu có kết quả thì đổ listKH đó vào frame Kết quả
     *    Nếu không có kết quả thì hiện dialog thông báo không tìm thấy
     */
    void timKhachHang(){
        String ChuoiTimKiem = txt_TimKhachHang.getText();
        if(ChuoiTimKiem.isEmpty()){
            txt_TimKhachHang.requestFocus();
        }
        else {
            try {
                List<KhachHang> ListKH = KhachHangDAO.timKhachHang(ChuoiTimKiem);
                DefaultTableModel dtm = (DefaultTableModel) tbl_DanhSachKhachHang.getModel();
                dtm.setRowCount(0);
                for (KhachHang KhachHang : ListKH) {
                    Object[] row = {
                        KhachHang.getMaKhachHang(),
                        KhachHang.getHoTen(),
                        KhachHang.getDiemThuong(),
                        KhachHang.getDienThoai(),
                        KhachHang.getDiaChi()
                    };
                    dtm.addRow(row);
                }
            } catch (Exception e) {
                DialogHelper.alert(this, "Có lỗi truy vấn!!!: " + e.toString());
            }
        }
    }
